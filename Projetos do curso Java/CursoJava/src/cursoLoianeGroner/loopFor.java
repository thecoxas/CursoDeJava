package cursoLoianeGroner;

public class loopFor {
    public static void main(String[] args) {
        
        for (int i = 0; i <= 10; i++) {
            System.out.println("i tem valor de: "+i);
        }
        for (int i = 0, j = 10; i <= j; i++, j--) {
            System.out.println("i tem valor de: "+i+" j tem valor de: "+j);

        }
    }
}
