package Curso_DevDojo_Introducao;

public class EstruturaCondicional03 {
    public static void main(String[] args) {
        //Operador ternário é um if e else simplificado
        double salario = 6000;
        // (Condição) ? verdadeiro ou falso
        String resultado = salario > 5000 ? "to rico" : "to pobre";

        System.out.println(resultado);

    }
}
